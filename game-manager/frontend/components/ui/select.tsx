export const SelectTrigger = React.forwardRef<
  React.ElementRef<typeof SelectPrimitive.Trigger>,
  React.ComponentPropsWithoutRef<typeof SelectPrimitive.Trigger>
>(({ className, children, ...props }, ref) => (
  <SelectPrimitive.Trigger
    ref={ref}
    className={cn(
      'font-display flex h-8 w-full items-center justify-between gap-2 rounded-md border border-theme-border bg-theme-card px-2 text-sm text-theme-text outline-none focus:border-theme-accent disabled:cursor-not-allowed disabled:opacity-50',
      className
    )}
    {...props}
  >
    {children}
    <SelectPrimitive.Icon asChild>
      <ChevronDown className="font-display h-4 w-4 text-theme-muted" />
    </SelectPrimitive.Icon>
  </SelectPrimitive.Trigger>
))
